								to amend the Administrative Adjudication of Road Traffic Offences Act, 1998, so as:
								<ul>
									<li>to substitute and insert certain definitions;</li>
									<li>to improve the manner of serving documents to infringers;</li>
									<li>to add to the functions of the Road Traffic Infringement Authority;</li>
									<li>to repeal certain obsolete provisions;</li>
									<li>to establish and administer rehabilitation programmes;</li>
									<li>to provide for the apportionment of penalties;</li>
									<li>to provide for the establishment of the Appeals Tribunal and matters related thereto; and</li>
									<li>to effect textual corrections.</li>
