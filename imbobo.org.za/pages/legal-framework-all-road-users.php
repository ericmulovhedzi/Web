					<h5 style="font-weight:normal;padding-bottom:3%;">Records management legislation:</h5>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/promotion-access-information-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/a2-000.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;The Promotion of Access to Information Act (Act No. 2 of 2000)</p>
					<p>
						The purpose of the Act is to promote transparency, accountability and effective governance by empowering and educating the public-
						<ul style="line-height:220%;padding-left:5%;">
							<li>to understand and exercise their rights;</li>
							<li>to understand the functions and operation of public bodies; and</li>
							<li>to effectively scrutinize, and participate in, decision-making by public bodies that affects their rights.</li>
						</ul>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/promotion-administrative-justice-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/a3-000.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;The Promotion of Administrative Justice Act (Act No. 3 of 2000)</p>
					<p>
						The purpose of the Act is to ensure that administrative action is lawful, reasonable and fair and properly documented.<br /><br />The Promotion of Administrative Justice Act imposes a duty on the state to ensure that administrative action is lawful, reasonable and procedurally fair; and everyone whose rights have been adversely affected by administrative action has the right to be given written reasons for such an action.
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/electronic-communications-and-transactions-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/a25-02.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;The Electronic Communications and Transactions Act (Act No. 25 of 2002)</p>
					<p>
						The purpose of the Act is to legalize electronic communications and transactions, and to built trust in electronic records. According to the Electronic Communications and Transactions Act data messages are legally admissible records, provided that their authenticity and reliability as true evidence of a transaction can be proven beyond any doubt. 
						<br /><br />
						The evidential weight of the electronic records (including e-mails) would depend amongst others on the reliability of the manner in which the messages were managed by the originator and the receiver. Should bodies not have a properly enforced records management and e-mail policy and a reliable and secure record keeping system, they run the risk that the evidential weight of their electronic records (including e-mails) might be diminished.
						<br /><br />
						The Electronic Communications and Transactions Act 25 of 2002 intends:
						<ul style="line-height:220%;padding-left:5%;">
							<li class="alert alert-secondary" style="padding:0% 0.75% 0% 0.75%;">to provide for the development of a national e-strategy for the Republic;</li>
							<li>to promote universal access to electronic communications and transactions and the use of electronic transactions by SMMEs;</li>
							<li>to provide for human resource development in electronic transactions;</li>
							<li>to prevent abuse of information systems;</li>
							<li class="alert alert-secondary" style="padding:0% 0.75% 0% 0.75%;">to encourage the use of e-government services; and</li>
						</ul>
					</p>
					
					<h5 style="font-weight:normal;padding-bottom:3%;">Roads legislation:</h5>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/national-land-transport-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/32110413.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;National Land Transport Act, No. 5 of 2009</p>
					<p>
						<ul style="line-height:220%;padding-left:5%;">
							<li>
								National Land Transport Act, No. 5 of 2009 requires amongst others that-
								<ol type="a" start="8">
									<li>
										Section 5(4)(h): The Minister must promote Public Transport that-
										<ol type="i">
											<li style="type:a">is safe; and</li>
											<li>is of an acceptable standard and readily accessible and is operated in conjuction with effective infrastructure.</li>
										</ol>
									</li>
									<li>Section 9(2)(i): An MEC must take active role in sourcing funding to promote objectives of this Act in the province.</li>
								</ol>
							</li>
						</ul>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/national-road-traffic-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/act93of1996.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;The National Road Traffic Act 93 of 1996</p>
					<p>
						The National Road Traffic Act 93 of 1996 intends:
						<ul style="line-height:220%;padding-left:5%;">
							<li>to provide for road traffic matters which shall apply uniformly throughout the Republic and for matters connected therewith.</li>
						</ul>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/road-accident-fund-act-0"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/act56of1996.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;Road Accident Fund Act 56 of 1996</p>
					<p>
						The Road Accident Fund Act 56 of 1996 intends:
						<ul style="line-height:220%;padding-left:5%;">
							<li>to provide for the establishment of the Road Accident Fund; and</li>
							<li>to provide for matters connected therewith.</li>
						</ul>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/administrative-adjudication-road-traffic-offences-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/a46-98.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;Administrative Adjudication of Road Traffic Offences Act 46 of 1998</p>
					<p>
						The Administrative Adjudication of Road Traffic Offences Act 46 of 1998 intends:
						<ul style="line-height:220%;padding-left:5%;">
							<li>to promote road traffic quality by providing for a scheme to discourage road traffic contraventions, to facilitate the adjudication of road traffic infringements, to support the prosecution of offences in terms of the national and provincial laws relating to road traffic, and implement a points demerit system;</li>
							<li>to provide for the establishment of an agency to administer the scheme;</li>
							<li>to provide for the establishment of a board to represent the agency; and</li>
						</ul>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/administrative-adjudication-road-traffic-offences-amendment-act-4-2019-19-aug-2019-0000"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201908/4264819-8act4of2019adminiadjudicroadtrafficoffencesamendact.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;Administrative Adjudication of Road Traffic Offences Amendment Act 4 of 2019</p>
