					  <h1>"Having trouble with potholes in SA? Here's all you need to know"</h1><br />
					  <p style="font-weight:normal;color:#dedede;font-style:italic;text-align: justify;">
						<img src="<? echo WWWROOT; ?>images/data/potholes/news24-pohole.jpeg"  class="rounded" style="width:35%;float:left;margin-right:3%;" />
						This is a conversation, <span style='color:#e13832;font-weight:normal;font-style:normal;font-size:150%;'>which inspired iMbobo NPC's mission</span>, that Charlen Raymond of <a class="text-reset fw-bold" target='_blank' href="https://www.news24.com/wheels/news/guides_and_lists/having-trouble-with-potholes-in-sa-heres-all-you-need-to-know-20161006"><span style='color:#897a1a;'>Wheels24</span></a> had with Howard Dembovsky, national chairman of Justice Project South Africa (JPSA), to discus the pothole situation in SA primarily in relation to the City of Cape Town's Transport for Cape Town (TCT), the City's transport authority.
						<br /><br /><span style='font-style:normal;color:#777;'>
						Author: Charlen Raymond<br />
						Date: 06 Oct 2016<br />
						Publisher: <a target='_blank' href="https://www.news24.com/wheels/news/guides_and_lists/having-trouble-with-potholes-in-sa-heres-all-you-need-to-know-20161006"><span style='color:#897a1a;'>News24, Wheels24</span></a><br />
					  </p>
					</div>
				</div>
		    
				<div class="how-it-works hide-small">
					<div class="steps">
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left">01: JPSA</div>What is a pothole and how is it formed?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								Roadways should be even surfaces free of holes. A pothole is a depression or hollow in a road surface caused by wear or subsidence.
								Generally, all potholes start of quite small and increase in size and depth as more and more vehicles drive over or into it.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left">02: JPSA</div>What is the process with regards to fixing a pothole?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								Road-building and maintenance is a job for properly qualified engineers and workers, using specialised equipment and materials. It is inadvisable for any unqualified person to do so since they can end up doing more damage than good. It's not as simple as pouring some concrete or tar into it and just like most people would not even think of trying to fill a dental cavity themselves. They really should leave this job to the professionals.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left">03: JPSA</div>Are there laws preventing the public from taking matters into their own hands?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								It is an offence to cause damage to the road surface, so theoretically, members of the public can be prosecuted for taking matters into their own hands.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left">04: JPSA</div>How are potholes fixed? How do we (the public) know if those assigned to remedy the issue are qualified?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								They (municipalities) hire road maintenance firms to do so, or have their own internalised road maintenance departments do so. Sadly, the public won't know whether the people fixing the pothole are qualified to do it, or not. They simply need to hope that the road authority has that in hand.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left">05: JPSA</div>Why is there a delay in repairs (i.e months)? Are there tender processes?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								There are tender processes for the awarding of road maintenance tasks, but the most likely reason for extended periods of non-repair can be attributed to lack of budget and/or resources to fix them. And in some cases, the fact that no-one has reported the pothole because people simply assume someone else has.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left" style='background-color:#0084af;'>06: TCT</div>How does the City go about fixing potholes?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								Transport for Cape Town (TCT), the City's transport authority, is responsible for the city's road network of approximately 10 629km. A total of 47 teams from 20 depots are responsible for repairing potholes and in at least 80% of the cases, the teams meet the City's own benchmark of fixing a pothole within 72 hours of it being reported to the Transport Information Centre.
								<br /><br />
								Residents report at least 250 potholes to the City's hotline every week and the other potholes are identified by the supervisory staff at TCT's depots.
								<br /><br />
								A C3 (fault reporting notification) is generated for every pothole that a resident reports to the City's fault reporting system. This report is sent to the relevant area depot who passes it on to the responsible crew to undertake the repairs. This administrative process can take up to a day, but in most cases it is sorted out within a matter of hours.
								<br /><br />
								The team will inspect the site and make the road safe for road users, either by repairing the pothole there and then or by demarcating the area if an immediate repair is not possible.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left" style='background-color:#0084af;'>07: TCT</div>Does the City outsource contractors or is there an in-house team that fixes potholes?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								The above-mentioned 47 teams from 20 TCT depots are responsible for repairing potholes on the city's roads.
								<br /><br />
								Importantly, the City, over the past 12 months, established <i style='color:#e13832;font-weight:normal;'>four all-female road repair teams</i> based at TCT depots in Ndabeni, Fish Hoek, Heideveld and Kuils River as part of the Expanded Public Works Programme (EPWP) within TCT. Thus far, up to 24 women have been trained to repair and do maintenance work on roads, footways and stormwater infrastructure in these areas.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left" style='background-color:#0084af;'>08: TCT</div>On average, what are the costs of fixing potholes?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								TCT has spent R464-million on road maintenance in the 2014/15 year, of which R110mn was allocated for the repair of potholes (the figures for 2015/16 are still being finalised). 
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left" style='background-color:#0084af;'>09: TCT</div>How many potholes do you fix per month?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								At least 260 potholes every week.
							</span>
						</div>
						<div class="step" style='width:100%;clear:both;'>
							<div class="rounded-left" style='background-color:#0084af;'>10: TCT</div>Where can the public report potholes?<br /><br />
							<span style='font-size:90%;font-weight:normal;color:#aeaeae;text-align: justify;'>
								Residents are requested to please report potholes to the Transport Information Centre on 0800 65 64 63 - it operates 24/7 and is toll-free from a landline. Call centre agents can assist callers in isiXhosa, English and Afrikaans. It is also important to give the correct location (street name and nearest corner) of the said pothole.
							</span>
						</div>
