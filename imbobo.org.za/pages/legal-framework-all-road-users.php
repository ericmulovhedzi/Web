								<strong>Section 24</strong> states amongst others as follows:
								<ol type="i">
									<li>The right to an environment that is not harmful to their health or well-being; and</li>
									<li>The right to have the environment protected, for the benefit of present and future generations.</li>
								</ol>
							</li>
							<li>
								<strong>Section 195</strong> of the Constitution provides amongst others for the:
								<ol type="i">
									<li>effective, economical and efficient use of resources;</li>
									<li>provision of timely, accessible and accurate information; and requires that</li>
