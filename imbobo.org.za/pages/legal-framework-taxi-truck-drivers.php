					<h5 style="font-weight:normal;padding-bottom:3%;">Constitutional laws:</h5>
					<p style="font-weight:bold;">Constitution of South African, 1996</p>
					<p>
						<ul style="line-height:220%;padding-left:5%;">
							<li>
								<strong>Section 23</strong> states amongst others as follows:
								<ol type="a" start="9">
									<li>Everyone has the right to fair labour practices.</li>
								</ol>
							</li>
						</ul>
