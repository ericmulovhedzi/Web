					<h5 style="font-weight:normal;padding-bottom:3%;">Labour and occupational related legislation:</h5>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/occupational-health-and-safety-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/act85of1993.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;Occupational Health and Safety Act, 85 of 1993</p>
					<p>
						<ol type="a" start="9" style="line-height:220%;padding-left:5%;">
							<li>To provide for the health and safety of persons at work as well as persons other than persons at work.</li>
						</ol>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/compensation-occupational-injuries-and-diseases-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/act130of1993.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;Compensation for Occupational Injury and Diseases Act, No. 61 of 1993</p>
					<p>
						The Compensation for Occupational Injuries and Diseases Act 130 of 1993 intends:
						<ul style="line-height:220%;padding-left:5%;">
							<li>to provide for compensation for disablement caused by occupational injuries or diseases sustained or contracted by employees in the course of their employment, or for death resulting from such injuries or diseases.</li>
						</ul>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/employment-equity-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/a55-980.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;Employment Equity Act, 55 of 1998</p>
					<p>
						Employment Equity Act, 55 of 1998 intends:
						<ul style="line-height:220%;padding-left:5%;">
							<li>to eliminate unfair discrimination in employement; and</li>
							<li>to promote economic development and efficiency in the workforce.</li>
						</ul>
					</p>
					<p style="font-weight:bold;"><a target="_blank" href="https://www.gov.za/documents/labour-relations-act"><i class="fa fa-external-link-alt"></i></a> &nbsp;&nbsp;<a target="_blank" href="https://www.gov.za/sites/default/files/gcis_document/201409/act66-1995labourrelations.pdf"><i class="fa fa-file-pdf" style="font-size:110%;"></i></a> &nbsp;&nbsp;Labour Relations Act, No. 66 of 1995</p>
					<p>
						Employment Equity Act, 55 of 1998 intends:
						<ul style="line-height:220%;padding-left:5%;">
							<li>to promote and facilitate collective bargaining at the workplace and at sectoral level;</li>
							<li>to promote employee participation in decision-making through the establishment of workplace forums;</li>
							<li>to provide simple procedures for the resolution of labour disputes through statutory conciliation, mediation and arbitration (for which purpose the Commission for Conciliation, Mediation and Arbitration is established), and through independent alternative dispute resolution services accredited for that purpose;</li>
							<li>to provide for a simplified procedure for the registration of trade unions and employers' organisations, and to provide for their regulation to ensure democratic practices and proper financial control; and</li>
							<li>to give effect to the public international law obligations of the Republic relating to labour relations.</li>
						</ul>
					</p>
					
