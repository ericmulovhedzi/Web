					<h3 style='color:#333;margin:0% 0% 2.5% 0%;font-weight:normal;'>Records Management</h3>
					<p>
						<img src="<? echo WWWROOT; ?>images/generic/pcv-files.png" style="float:right;width:45%;" />
						As a pothole reporting tool, iMbobo App NPC is, in a sense, a records management system. What this actually means is that, we are essentially in the business of managing different individuals and organizations information or records as well as safeguarding them against cybers security risks.</p>
						Therefore, Sound Records Management is imperative in this contex to ensure that our users's records are managed in terms of an organisational records management programme governed by an organisational records management policy.
					</p>
					
					<h5 style="font-weight:normal;padding:0% 0% 1% 0%;">What is Records Management?</h5>
					<p>Records management is a process of ensuring the proper creation, maintenance, use and disposal of records to achieve efficient, transparent and accountable actions.</p>
					<p>It is for these reasons that iMbobo App NPC's legal department decided to take initiatives to design and compile a sound Records Management System and policies to ensure that our customers or business partners records are properly managed, used and disposed of in a manner that is efficient and transparent.</p>
					<p>
						An organizational electronic e-Registry File Classification Plan was then developed in order to leverage upon the following benefits:<br />
						<li style="margin-left:2.5%;">Proper file referencing and indexing.</li>
						<li style="margin-left:2.5%;">A central repository of the information that is available to all staff connected to the system and therefore no need for temporary file.</li>
						<li style="margin-left:2.5%;">Documentation can be taken off-site, as a digital registry will be available to staff of the organization irrespective of where they are housed.</li>
						<li style="margin-left:2.5%;">Access to the information that is more controlled.</li>
						<li style="margin-left:2.5%;">Access to the information that is much fast and efficient.</li>
						<li style="margin-left:2.5%;">There is always a digital copy of the original documentation, in the event of fire or any other disaster.</li>
						<li style="margin-left:2.5%;">Staff will always be able to access the same information at the same time hence, no time wasted, and waiting for documentation to become available.</li>
						<li style="margin-left:2.5%;">Staff now have more time to perform other tasks.</li>
						<li style="margin-left:2.5%;">Auditing is made easier.</li>
						<li style="margin-left:2.5%;">No more missing, misfiled or lost documentation.</li>
						<li style="margin-left:2.5%;">The HR and Finance section now have more control over their sensitive staff information.</li>
					</p>
					<p>
						As a way to complete the life-cycle of our file classification plan, we deployed <b>The Vault</b>; our organization's and some of our customer's disposed data is stored in this offline, firmware-based environment in one of our two data ware houses together with other physical records in Lever Arch Files. <b>The Vault</b> is our own version of secure data storage. We deployed this product in-house to safeguard our customers' data as one of the downsides of cloud-based backups is that they are susceptible to ransomware.
					</p>
					<p>The synergy between our legal and governance team produced a file plan with different disposal symbols to ensure that different records are disposed of exactly according to their life-cycle expiry date. This was integrated with an in-house Electronic Document Management System (EDMS) strictly developed to suit both the easy-document-access and security conditions of our firmware-based Vault environment.</p>
					<p>
						During the appraisal phase of the file plan development, careful consideration was given to all records management legal requirements as envisaged in the South African Constitution, 1996 and some but not limited to the following statutory regulations:<br />
						<li style="margin-left:2.5%;">Constitution of the Republic of South Africa, Act No. 108 of 1996 (Section 32(1)(a), and also Section 41(1)(c) and Section 195(1)(f))</li>
						<li style="margin-left:2.5%;">Promotion of Access to Information Act, No. 2 of 2000 (Section 14(1) to (4) and Section 51(1) to (4)): Section 51 for Private Entities</li>
						<li style="margin-left:2.5%;">Companies Act No. 61 of 1973</li>
						<li style="margin-left:2.5%;">Income Tax Act, No. 58 of 1962 (Section 75): Amendment of 1 January 1993 for all accounting records retention period</li>
						<li style="margin-left:2.5%;">Basic Conditions of Employment Act, No. 75 of 1997 (Section 29)</li>
						<li style="margin-left:2.5%;">Labour Relations Act, 66 of 1995 (Section 53, 54 and Section 98, 99)</li>
						<li style="margin-left:2.5%;">Occupational Health and Safety Act, No. 85 of 1993</li>
						<li style="margin-left:2.5%;">Compensation for Occupational Injuries and Diseases Act, No. 130 of 1993</li>
						<li style="margin-left:2.5%;">Employment Equity Act</li>
